    public ProceduralFont(GraphicsDevice graphicsDevice)
    {
        int numChars = 95;
        int texWidth = numChars * GlyphWidth;
        int texHeight = GlyphHeight;

        _fontTexture = new Texture2D(graphicsDevice, texWidth, texHeight);
        Color[] pixelData = new Color[texWidth * texHeight];

        // Fill background with transparent
        for (int i = 0; i < pixelData.Length; i++)
            pixelData[i] = Color.Transparent;

        // Render glyphs into texture data
        for (int c = 0; c < numChars; c++)
        {
            int fontDataIndex = c * 5;
            for (int col = 0; col < 5; col++)
            {
                byte colByte = FontData[fontDataIndex + col];
                int x = c * GlyphWidth + col; // 0..4 columns. columns 5, 6, 7 are padding
                for (int r = 0; r < 7; r++)
                {
                    if ((colByte & (1 << r)) != 0)
                    {
                        int y = r;
                        pixelData[x + y * texWidth] = Color.White;
                    }
                }
            }
        }

        _fontTexture.SetData(pixelData);
    }

    public void DrawString(SpriteBatch spriteBatch, string text, Vector2 position, Color color, float scale = 1f)
    {
        if (string.IsNullOrEmpty(text))
            return;

        Vector2 curPos = position;
        for (int i = 0; i < text.Length; i++)
        {
            char ch = text[i];
            if (ch == '\n')
            {
                curPos.X = position.X;
                curPos.Y += GlyphHeight * scale * 1.25f;
                continue;
            }

            int charIdx = ch - CharOffset;
            if (charIdx < 0 || charIdx >= 95)
                charIdx = 0; // fallback to space

            Rectangle srcRect = new Rectangle(charIdx * GlyphWidth, 0, GlyphWidth, GlyphHeight);
            spriteBatch.Draw(_fontTexture, curPos, srcRect, color, 0f, Vector2.Zero, scale, SpriteEffects.None, 0f);

            // Spacing: 6 pixels (5 columns of glyph data + 1 pixel of spacing) * scale
            curPos.X += 6 * scale;
        }
    }

    public Vector2 MeasureString(string text, float scale = 1f)
    {
        if (string.IsNullOrEmpty(text))
            return Vector2.Zero;

        float maxX = 0f;
        float curX = 0f;
        float curY = GlyphHeight * scale;

        for (int i = 0; i < text.Length; i++)
        {
            char ch = text[i];
            if (ch == '\n')
            {
                maxX = MathF.Max(maxX, curX);
                curX = 0f;
                curY += GlyphHeight * scale * 1.25f;
                continue;
            }

            curX += 6 * scale;
        }

        maxX = MathF.Max(maxX, curX);
        return new Vector2(maxX, curY);
    }

    public void Dispose()
    {
        _fontTexture?.Dispose();
        _fontTexture = null;
    }
}
